public class Calculadora{
	public static void main(String[] args) {
	double a = 10.5;
	double b = 5.2;
	
	System.out.print(a + " + " + b + " = " + (a+b));
	System.out.print(a + " - " + b + " = " + (a-b));
	System.out.print(a + " * " + b + " = " + (a*b));
	System.out.print(a + " / " + b + " = " + (a/b));
	}
}